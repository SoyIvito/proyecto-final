#PURE DE PAPAS: https://cookpad.com/ar/recetas/23909118?ref=search&search_term=pure+de+papas
def r_pure():
    print("Ingredientes:\n 4 papas grandes\n Manteca a gusto (80g aprox)\n Sal y pimienta a gusto\n 1 puñado de " \
    "perejil\n 1 diente de ajo grande\n Opcional jugo de limon")
    print("1. Lavamos y pelamos las papas. Cortamos en cubos grandes, colocamos en una olla con abundante agua con sal.\n Llevamos a hervor hasta que estén tiernas, unos 20 minutos dependiendo del tamaño y la cantidad.")
    print("2. Mientras picamos finamente el ajo y el perejil. Reservamos")
    print("3. Colocamos en caliente, pisamos con ayuda de un pisaáás hasta obtener un pure liso.\n Añadimos la manteca y la pimienta. Rectificamos sazon si lo necesitara.\n Mezclamos y añadimos el ajo y el perejil picados. Integrar bien")

#GUISO DE LENTEJAS: https://cookpad.com/ar/recetas/24901620?ref=search&search_term=guiso
def r_guiso():
    print("Ingredientes:\n 500g lentejas\n 1 cebolla\n 3 cebolla de verdeo\n 2 papas\n 2 puerros\n 1 zanahoria\n 100g panceta" \
    "ahumada\n 1 chorizo colorado\n 2 tomates maduros\n condimentos varios\n 3 cubos de caldo")
    print("1. En una cacerola poner las lentejas (previas en remojo toda la noche, las\n papas cortadas en cubos y las zanahorias en rodajas. Agregar\n agua hasta tapar los ingredientes. Tambien poner el caldo)")
    print("2. Mientras tanto en otra sarten, hacer el sofrito: panceta, cebolla, puerro y tomate")
    print("3. Cuando la cebolla y el puerro esten transparentes, agregamos el sofrito a la cacerola\n con las lentejas. Cocinamos 10 mins mas hasta que esten tiernas")
    print("4. Apagamos el fuego, dejamos reposar unos minutos y listo!")

#TORTILLA DE PAPAS: https://cookpad.com/ar/recetas/24807813?ref=search&search_term=tortilla+de+papas
def r_tortilla_papas():
    print("Ingredientes:\n 5 papas grandes\n 6 huevos\n Oregano\n Aceite para freir\n Sal")
    print("1. Despues de lavar y pelar las papas, las cortamos en cubos (o en la forma de preferencia)")
    print("2. Freir las papas y reservar")
    print("3. Colocamos en un bowl las papas fritas y le agregamos oregano y sal a gusto.\n Mezclamos para que agarre sabos. Posteriormente le\n agregamos los 6 huevos (varian segun la cantidad y necesidad de papas)")
    print("4. Colocamos la mezcla en una sarten caliente con aceita y la damos vuelta y\n vuelta hasta que este dorada. Retiramos del fuego")

