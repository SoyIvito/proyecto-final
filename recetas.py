#RECETA MILANESA DE POLLO: https://cookpad.com/ar/r/24591349
def r_milanesa_pollo():
    print("Ingredientes:\n 3 pechugas de pollo\n 2 huevos\n 1 pizca de comino\n 1 pizca de aji molido\n " \
    "1 pizca de provenzal o ajo y perejil\n 1 pizca de sal a gusto\n 500g de pan rallado")
    print("1. Batir los huevos y despues agregar los condimentos uno por uno")
    print("2. Y a las pechugas filetear y agregarle sal a cada una de \n las pechugas y después agregarlas a la mezcla con huevo y batir todo")
    print("3. Despues poner en una fuente todo el pan rallado e ir empenando las milanesas")
    print("4. Luego fritar o cocinar en el horno (Horno 20/25 mins) (Fritura: 15 mins)")

